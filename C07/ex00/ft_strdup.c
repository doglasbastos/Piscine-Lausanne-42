#include<stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;
	int		size;

	size = 0;
	while (src[size] != '\0')
	{
		size++;
	}
	dest = malloc(sizeof(char) * (size + 1));
	if (dest == NULL)
	{
		return (NULL);
	}
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
